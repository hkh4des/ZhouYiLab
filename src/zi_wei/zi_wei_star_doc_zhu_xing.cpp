    string DualStarDocument::to_string() const {
        ostringstream oss;
        
        oss << "【" << name << "】\n";
        oss << "━━━━━━━━━━━━━━━━━━━━━━━━━━\n";
        
        oss << "组合星曜: " << star1 << " + " << star2 << "\n";
        oss << "命宫出现概率: ≈" << fixed << setprecision(2) << probability << "%\n";
        
        if (!ge_ju.empty()) {
            oss << "格局: " << ge_ju << "\n";
        }
        
        oss << "━━━━━━━━━━━━━━━━━━━━━━━━━━\n";
        
        if (!key_trait.empty()) {
            oss << "核心特质: " << key_trait << "\n";
        }
        
        if (!description.empty()) {
            oss << "\n" << description << "\n";
        }
        
        return oss.str();
    }

