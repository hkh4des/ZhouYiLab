    string get_zhu_xing_category_description(ZhuXingCategory category) {
        auto it = ZHU_XING_CATEGORY_DESCRIPTIONS.find(category);
        if (it != ZHU_XING_CATEGORY_DESCRIPTIONS.end()) {
            return it->second;
        }
        return "";
    }

    vector<string> get_zhu_xing_by_category(ZhuXingCategory category) {
        auto it = ZHU_XING_CATEGORY_STARS.find(category);
        if (it != ZHU_XING_CATEGORY_STARS.end()) {
            return it->second;
        }
        return {};
    }

    vector<CategoryInfo> get_all_zhu_xing_categories() {
        vector<CategoryInfo> result;
        
        for (int i = 0; i < static_cast<int>(ZhuXingCategory::COUNT); ++i) {
            auto cat = static_cast<ZhuXingCategory>(i);
            CategoryInfo info;
            info.name = string(Mapper::to_zh(cat));
            
            auto desc_it = ZHU_XING_CATEGORY_DESCRIPTIONS.find(cat);
            if (desc_it != ZHU_XING_CATEGORY_DESCRIPTIONS.end()) {
                info.description = desc_it->second;
            }
            
            auto stars_it = ZHU_XING_CATEGORY_STARS.find(cat);
            if (stars_it != ZHU_XING_CATEGORY_STARS.end()) {
                info.stars = stars_it->second;
            }
            
            result.push_back(info);
        }
        
        return result;
    }

    optional<StarDocument> get_zhu_xing_document(const string& star_name) {
        auto it = ZHU_XING_DATABASE.find(star_name);
        if (it != ZHU_XING_DATABASE.end()) {
            return it->second;
        }
        return nullopt;
    }

    optional<ZhuXingCategory> get_zhu_xing_category(const string& star_name) {
        // 检查紫微星系
        auto zi_wei_stars = ZHU_XING_CATEGORY_STARS.at(ZhuXingCategory::ZiWeiXing);
        if (find(zi_wei_stars.begin(), zi_wei_stars.end(), star_name) != zi_wei_stars.end()) {
            return ZhuXingCategory::ZiWeiXing;
        }
        
        // 检查天府星系
        auto tian_fu_stars = ZHU_XING_CATEGORY_STARS.at(ZhuXingCategory::TianFuXing);
        if (find(tian_fu_stars.begin(), tian_fu_stars.end(), star_name) != tian_fu_stars.end()) {
            return ZhuXingCategory::TianFuXing;
        }
        
        return nullopt;
    }

    vector<string> get_all_zhu_xing_names() {
        vector<string> result;
        for (const auto& [name, doc] : ZHU_XING_DATABASE) {
            result.push_back(name);
        }
        return result;
    }

    optional<DualStarDocument> get_dual_star_document(const string& star1, const string& star2) {
        for (const auto& doc : DUAL_STAR_DATABASE) {
            if ((doc.star1 == star1 && doc.star2 == star2) ||
                (doc.star1 == star2 && doc.star2 == star1)) {
                return doc;
            }
        }
        return nullopt;
    }

    vector<DualStarDocument> get_dual_star_combinations(const string& star_name) {
        vector<DualStarDocument> result;
        for (const auto& doc : DUAL_STAR_DATABASE) {
            if (doc.star1 == star_name || doc.star2 == star_name) {
                result.push_back(doc);
            }
        }
        return result;
    }

    vector<DualStarDocument> get_all_dual_star_combinations() {
        return DUAL_STAR_DATABASE;
    }

